num1 = int(input("what is you first number?\n"))

num2 = int(input("What is your second number?\n"))

operation = input("Choose an operation +, -, *, /\n")

if operation == "*":
     print(num1 * num2)
if operation == "/":
    print(num1 / num2)
if operation == "+":
    print(num1 + num2)
if operation == "-":
    print(num1 - num2)
